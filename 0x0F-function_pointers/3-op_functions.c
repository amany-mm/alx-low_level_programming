/**
 * op_add - add 2 int numbers
 * @a: 1st int number
 * @b: 2nd int number
 * Return: adding result
*/
int op_add(int a, int b)
{
	return (a + b);
}


/**
 * op_sub - subtract 2 int numbers
 * @a: 1st int number
 * @b: 2nd int number
 * Return: subtracting result
*/
int op_sub(int a, int b)
{
	return (a - b);
}


/**
 * op_mul - multiply 2 int numbers
 * @a: 1st int number
 * @b: 2nd int number
 * Return: multiplying result
*/
int op_mul(int a, int b)
{
	return (a * b);

}


/**
 * op_div - divide 2 int numbers
 * @a: 1st int number
 * @b: 2nd int number
 * Return: dividing result
*/
int op_div(int a, int b)
{
	return (a / b);
}


/**
 * op_mod - modulus 2 int numbers for reminder
 * @a: 1st int number
 * @b: 2nd int number
 * Return: modulus result
*/
int op_mod(int a, int b)
{
	return (a % b);
}
